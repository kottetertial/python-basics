# Addition, subtraction
print(11 + 2)
print(11 - 2)

# Multiplication, exponentiation
print(11 * 2)
print(11 ** 2)

# Division, integer division, modulo division
print(11 / 2)
print(11 // 2)
print(11 % 2)

# When will you wake up if you go to bed at 23:00 and sleep for 7 hrs?
print((23 + 7) % 24)

# When do you have to go to bed if you need to sleep for 7 hrs and wake up at 06:00?
print((6 - 7) % 24)

# Strings
print(2 * "2")
print("2" + "2")

